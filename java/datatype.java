//datatype in java 

class datatype
{
	public static void main(String args[])
	{
		int a = 10;
		char ch='t';
		double d = 3.445;
		System.out.println(a);
		System.out.println("hello welcome to java");
		System.out.println(ch);
		System.out.println(d);
		float f=1.233f;
		System.out.println(f);
	}
}